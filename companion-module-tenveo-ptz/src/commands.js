/* Reply parsers */

/** Parse a reply payload that begins with 0xY0 0x50 ... 0xFF and returns the data bytes. */
export function parseInqReply(buf) {
	if (!buf || buf.length < 3) return null
	if ((buf[0] & 0xf0) !== 0x90) return null
	if (buf[1] !== 0x50) return null
	const end = buf.indexOf(0xff)
	return Array.from(buf.slice(2, end < 0 ? buf.length : end))
}

/** Decode four 4-bit nibbles into a 16-bit value. */
export function denibble16(nibbles) {
	if (!nibbles || nibbles.length < 4) return 0
	return ((nibbles[0] & 0x0f) << 12) | ((nibbles[1] & 0x0f) << 8) | ((nibbles[2] & 0x0f) << 4) | (nibbles[3] & 0x0f)
}
