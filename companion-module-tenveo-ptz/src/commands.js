/**
 * Tenveo VISCA command builders.
 *
 * All commands start with 0x81 (camera ID 1) and end with 0xFF.
 * The ViscaIP layer will substitute the camera address byte at TX time
 * based on the user's configured Camera ID.
 *
 * Values that look "weird" (e.g. encoding 16-bit values as 4 nibbles 0p 0q 0r 0s)
 * are part of the VISCA spec — each nibble is the low 4 bits of a byte.
 */

const u4 = (v) => v & 0x0f

/** Encode a 16-bit value as four 4-bit nibbles (0p 0q 0r 0s). */
export function nibble16(v) {
	const x = v & 0xffff
	return [u4(x >> 12), u4(x >> 8), u4(x >> 4), u4(x)]
}

/* ───────── Pan / Tilt / Zoom ───────── */

export const PT_DIR = {
	UP: [0x03, 0x01],
	DOWN: [0x03, 0x02],
	LEFT: [0x01, 0x03],
	RIGHT: [0x02, 0x03],
	UP_LEFT: [0x01, 0x01],
	UP_RIGHT: [0x02, 0x01],
	DOWN_LEFT: [0x01, 0x02],
	DOWN_RIGHT: [0x02, 0x02],
	STOP: [0x03, 0x03],
}

/**
 * Pan/Tilt drive. `dir` is one of PT_DIR.
 * panSpeed: 1-24 (0x01-0x18), tiltSpeed: 1-20 (0x01-0x14)
 */
export function ptDrive(dir, panSpeed = 12, tiltSpeed = 10) {
	const [p, t] = dir
	const vv = Math.min(Math.max(panSpeed, 1), 24)
	const ww = Math.min(Math.max(tiltSpeed, 1), 20)
	return [0x81, 0x01, 0x06, 0x01, vv, ww, p, t, 0xff]
}

export const ptHome = () => [0x81, 0x01, 0x06, 0x04, 0xff]
export const ptReset = () => [0x81, 0x01, 0x06, 0x05, 0xff]

/** Absolute pan/tilt position (24-bit signed each, but using 16-bit field). */
export function ptAbsolute(pan, tilt, panSpeed = 12, tiltSpeed = 10) {
	const pp = pan < 0 ? (0x10000 + pan) & 0xffff : pan & 0xffff
	const tt = tilt < 0 ? (0x10000 + tilt) & 0xffff : tilt & 0xffff
	const [p1, p2, p3, p4] = nibble16(pp)
	const [t1, t2, t3, t4] = nibble16(tt)
	return [0x81, 0x01, 0x06, 0x02, panSpeed, tiltSpeed, p1, p2, p3, p4, t1, t2, t3, t4, 0xff]
}

/** Relative pan/tilt move. Pan/Tilt can be negative; two's complement for VISCA. */
export function ptRelative(pan, tilt, panSpeed = 12, tiltSpeed = 10) {
	const pp = pan < 0 ? (0x10000 + pan) & 0xffff : pan & 0xffff
	const tt = tilt < 0 ? (0x10000 + tilt) & 0xffff : tilt & 0xffff
	const [p1, p2, p3, p4] = nibble16(pp)
	const [t1, t2, t3, t4] = nibble16(tt)
	return [0x81, 0x01, 0x06, 0x03, panSpeed, tiltSpeed, p1, p2, p3, p4, t1, t2, t3, t4, 0xff]
}

